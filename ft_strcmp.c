#include "libft.h"

int	ft_strcmp(char const *s1, char const *s2)
{
	int diff;

	while (*s1 == *s2 && *s1 != '\0')
	{
		s1++;
		s2++;
	}
	diff = (unsigned char)*s1 - (unsigned char)*s2;
	return (diff);
}
